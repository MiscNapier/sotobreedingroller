// create tabby list automatically
/**
 * @param {string} rarity
 */
function getTabby(rarity) {
  let output = [];

  for (let i = 0; i < listMarkings[rarity].length; i++) {
    if (listMarkings[rarity][i][0].search(/tabby/) !== -1) {
      let gene = listMarkings[rarity][i][1];
      output.push(`n${gene}|${gene}${gene}`);
    }
  }

  return output.join("|");
}

